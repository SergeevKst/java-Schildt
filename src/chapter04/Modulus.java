package chapter04;

public class Modulus {
    public static void showModulus() {
        System.out.println("Операция деления по модулю % .");
        int x = 42;
        double y = 42.25;

        System.out.println("x mod 10 = " + x % 10);
        System.out.println("y mod 10 = " + y % 10);
    }
}
